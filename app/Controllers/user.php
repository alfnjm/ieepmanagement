    public function editProfile()
    {
        $session = session();

        if (!$session->get('isLoggedIn')) {
            return redirect()->to('/auth/login');
        }

        $userModel = new UserModel();
        $userId = $session->get('id');
        $user = $userModel->find($userId);

        return view('user/profile', [
